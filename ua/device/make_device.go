func genShell(b *bytes.Buffer) error {
	fmt.Fprintln(b)
	fmt.Fprintf(b, " var deviceShellRe= MustCompile(`%s`)\n", ua.Clean(`[a-z]+[ _]Shell[ _]\w{6}`))
	return generic(b, "Shell", "device/shell_tv.yml")
}
func generic(b *bytes.Buffer, name string, path string) error {
	items, err := loadDevice(path)
	if err != nil {
		return err
	}
	var s strings.Builder
	for i, d := range items {
		if i != 0 {
			s.WriteByte('|')
		}
		s.WriteString(d.Regex)
	}
	var buf bytes.Buffer

	if ua.IsStdRe(s.String()) {
		fmt.Fprintf(b, " var device%sAllRe= MustCompile(`%s`)\n", name, ua.Clean(s.String()))
	} else {
		fmt.Fprintf(b, " var device%sAllRe= MustCompile2(`%s`)\n", name, ua.Clean(s.String()))
	}
	fmt.Fprintf(b, "var device%sAll=[]*deviceRe{\n", name)
	for _, d := range items {
		buf.Reset()
		r := ua.Clean(d.Regex)
		if ua.IsStdRe(d.Regex) {
			fmt.Fprintf(&buf, "re:MustCompile(`%s`)", r)
		} else {
			fmt.Fprintf(&buf, "re2: MustCompile2(`%s`)", r)
		}
		fmt.Fprintf(b, "{%s,company:%q, ", &buf, d.Manufacturer)
		if d.Model != "" {
			fmt.Fprintf(b, "model:%q},\n", d.Model)
		} else {
			if len(d.Models) > 0 {
				fmt.Fprintf(b, "models:[]*deviceModel{")
				for _, m := range d.Models {

					fmt.Fprintf(b, "{model:%q,", m.Model)
					if ua.IsRe(m.Regex) {
						buf.Reset()
						r = ua.Clean(m.Regex)
						if ua.IsStdRe(m.Regex) {
							fmt.Fprintf(&buf, "re:MustCompile(`%s`)", r)
						} else {
							fmt.Fprintf(&buf, "re2: MustCompile2(`%s`)", r)
						}
						fmt.Fprintf(b, "%s},\n", &buf)
					} else {
						fmt.Fprintf(b, "exact:%q},\n", m.Regex)
					}
				}
				fmt.Fprintf(b, "},")
			}
			fmt.Fprintf(b, "},\n")
		}

	}
	fmt.Fprintln(b, "}")

	return nil
}

func loadDevice(path string) ([]*DeviceReg, error) {
	var v map[string]*DeviceReg
	err := ua.Read(path, &v)
	if err != nil {
		return nil, err
	}

	var items []*DeviceReg
	for key, value := range v {
		value.Manufacturer = key
		items = append(items, value)
	}
	sort.Sort(DeviceRegSlice(items))
	return items, nil
}
